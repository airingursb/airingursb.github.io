// V22.0 — Pixel-art UI icon registry.
//
// Every icon is a 16×16 ASCII grid. We render to inline SVG (1 unit per
// "pixel") with `shape-rendering: crispEdges` so it stays sharp at any
// CSS size. Stroke colours use `currentColor` so icons inherit the
// button's text colour — they go bright on hover, dim when disabled.
//
// Grid chars:
//   '.' = transparent
//   '1' = outline (currentColor)
//   '2' = body fill (currentColor opacity .55)
//   '3' = accent (var --icon-accent, default warm yellow)
//   'a' = currentColor opacity .35
//   '4' = currentColor opacity .25 (dim outline / shading)
//   'd' = accent opacity .7
// V22.4-review I2 — the legacy 'l' (lowercase L) character was visually
// indistinguishable from '1' in the source grids; renamed to '4'.
//
// Icons are declared inline (one tagged-template per name). The
// `iconSvg(name)` function returns a string of <svg>…</svg> ready to
// drop into innerHTML. `hydrateIcons(root?)` scans the DOM for
// `[data-icon]` elements and replaces them with the rendered SVG.

